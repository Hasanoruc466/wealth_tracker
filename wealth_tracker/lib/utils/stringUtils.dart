String getGoldName(var key){
  switch(key){
    case 'gram-has-altin':
      return 'Has Altın';
    case 'ceyrek-altin':
      return 'Çeyrek Altın';
    case 'yarim-altin':
      return 'Yarım Altın';
    case 'tam-altin':
      return 'Tam Altın';
    case 'cumhuriyet-altini':
      return 'Cumhuriyet Altını';
    case 'ata-altin':
      return 'Ata Altın';
    case '14-ayar-altin':
      return '14 Ayar Altın';
    case '18-ayar-altin':
      return '18 Ayar Altın';
    case '22-ayar-altin':
      return '22 Ayar Altın';
    case 'resat-altin':
      return 'Reşat Altın';
    case 'gram-altin':
      return "Gram Altın";
    case 'gumus':
      return 'Gümüş';
    case 'gram-platin':
      return 'Platin';
    default:
      return "";
  }
}